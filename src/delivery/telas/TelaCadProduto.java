     private void lista(){
        RNProduto rnProd = new RNProduto(); //CRIAR A INSTANCIA DA FACHADA
        DefaultTableModel modelo = new DefaultTableModel(); // INSTANCIA O OBJETO PADRÃO DE TABELA - ADICIONE A IMPORTAÇÃO
        ArrayList<Produto> lista = new ArrayList(); // CRIA UMA LISTA, DE ARRAYLIST DE LIVRO
        try {  
            lista = rnProd.listar(); // BUSCA OS DADOS DO BANCO PARA A LISTA
        } catch (Exception ex) {
            
        }
        tbProduto.getColumnModel().getColumn(2).setCellRenderer(new CurrencyTableCellRenderer()); // COLOCA COLUNA VALOR EM REAL
       modelo = (DefaultTableModel) tbProduto.getModel(); // CHECA O JTABLE PARA RECEBER OS DADOS
        if(modelo.getRowCount() > 0){  //se existir linha 
            modelo.setRowCount(0); // apaga todas as linhas
        }
        for (Produto produto : lista) { 
            modelo.addRow(new Object[]{produto.getCodProduto(),produto.getDescricao(),produto.getValor()});
        } // TRAS AS INFORMAÇÕES NA TABELA COM OS DADOS SOLICITADOS.
        
    }

