    private void tbProdutoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbProdutoMouseClicked
       
        
        tbProduto.addMouseListener(new MouseAdapter(){
         private int linha;
            
            @Override  
            public void mouseClicked(MouseEvent e) {  
 
                        linha = tbProduto.getSelectedRow();
                        produto.setCodProduto(Integer.parseInt((String.valueOf(tbProduto.getValueAt(linha, 0)))));  
                        produto.setDescricao(String.valueOf(tbProduto.getValueAt(linha, 1))); 
                        produto.setValor(Double.parseDouble((String.valueOf(tbProduto.getValueAt(linha, 2)))));

                txtDescricaoProduto.setText(produto.getDescricao());
                txtValorProduto.setText(String.valueOf(produto.getValor()));

            }  
        });
    }//GEN-LAST:event_tbProdutoMouseClicked

